    private void IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarActionPerformed
        // TODO add your handling code here:
        CargarDatosTel();
        CargarDatosCupones();
        CargarDatosLibrosVentas();
        cargarDatos();
        
        
        String usua = nomUsuario.getText();
        String contra = password.getText();
        
        boolean existe = false;
        
        for (USUARIO u : ProyectoFinal.usuario){
            if (u.getUsuario().equals(usua) && u.getPassword().equals(contra)){
         
            existe = true; 
            
            ProyectoFinal.usuarios = u;
            
            if (u.getRol().equals("Vendedor")) {
                String nombreVendedor = u.getNombre();
                ProyectoFinal.nombreVendedor = nombreVendedor; 
            }
            
            if (u.getRol().equals("Administrador")){
                
                Vent_Admon v = new Vent_Admon(u);
                v.setVisible(true);
            } else {
                Vent_Vendedor v = new Vent_Vendedor(u);
                
                v.setVisible(true);
            } 
            break;
        }  
        }
           if (existe == false){
            JOptionPane.showMessageDialog(this, "Usuario o Contraseña Incorrecto");
           
        }
         Limpiar();
    }//GEN-LAST:event_IngresarActionPerformed

