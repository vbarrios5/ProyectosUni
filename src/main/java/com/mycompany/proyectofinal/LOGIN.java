    public static void cargarDatos (){
        ProyectoFinal.usuario.clear();
        
        USUARIO us = new USUARIO();
        us.setNombre("Admin");
        us.setPassword("Admin");
        us.setUsuario("Admin");
        us.setRol("Administrador");
        
        ProyectoFinal.usuario.add(us);
        
        try (BufferedReader br = new BufferedReader(new FileReader ("Usuario.txt"))){
            String linea;
            while ((linea = br.readLine()) != null) {
            String [] datos = linea.split(",");
            if (datos.length == 4){
                USUARIO u = new USUARIO();
                u.setNombre(datos [0]);
                u.setUsuario(datos [1]);
                u.setRol(datos [2]);
                u.setPassword(datos [3]);
                
                ProyectoFinal.usuario.add(u);
            }
        }
        }catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Usuario no encontrado");
        }
    }
    
    
    public static void CargarDatosLibrosVentas (){
        ProyectoFinal.CrearVentas.clear();
        ProyectoFinal.librosVentas.clear();
        
        try (BufferedReader br = new BufferedReader(new FileReader ("LibrosVentas.txt"))){
            String linea;
            while ((linea = br.readLine()) != null) {
            String [] datos = linea.split(",");
            if (datos.length == 5){
                L_VENTAS li = new L_VENTAS();
                li.setTitulo(datos [0]);
                li.setAutor(datos [1]);
                li.setGenero(datos [2]);
                li.setPrecio(Double.parseDouble(datos [3]));
                li.setCantidad(datos [4]);
                
                ProyectoFinal.librosVentas.add(li);
            }
        }
        }catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Libro no encontrado");
        }
    }
        
    public static void CargarDatosCupones(){
        ProyectoFinal.cupones.clear();
        
        try(BufferedReader br = new BufferedReader(new FileReader ("Cupones.txt"))){
            String linea;
            while ((linea = br.readLine()) != null){
            String [] datos = linea.split(","); 
            if (datos.length == 4){
                CUPONES c = new CUPONES();
                c.setCodigoDescuento(datos [0]);
                c.setDescuento(datos [1]);
                c.setTipoDescuento(datos [2]);
                c.setVencimiento(datos [3]);
                
                ProyectoFinal.cupones.add(c);
            }
        }
            
        }catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Libro no encontrado");
        }
        
    }
    
    public static void CargarDatosTel(){
        ProyectoFinal.telefonos.clear();
        
        try(BufferedReader br = new BufferedReader(new FileReader ("Telefono.txt"))){
            String linea;
            while ((linea = br.readLine()) != null){
            String [] datos = linea.split(","); 
            if (datos.length == 2){
                TELEFONO t = new TELEFONO();
                t.setCodigo(datos [0]);
                t.setNumero(datos [1]);
                
                ProyectoFinal.telefonos.add(t);
            }
        }
            
        }catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Telefono no encontrado");
        }
    }
    
    public USUARIO UsuarioPrincipal() {
        String usuarios = nomUsuario.getText();
    
        if (usuarios.equals("Admin")){
            JOptionPane.showMessageDialog(this, "Al Administrador no "
                    + "se le puede cambiar contraseña");
            return null;
        } else { 
          for (USUARIO u : ProyectoFinal.usuario) {
        if (u.getUsuario().equals(usuarios)) {
            return u;  
        }
          }
    }
    return null;   
        
    }
    
    
    
